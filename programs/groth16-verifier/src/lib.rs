/// Scalar field size (r) for BN254
/// r = 21888242871839275222246405745257275088548364400416034343698204186575808495617
const SCALAR_FIELD: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29,
    0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x28, 0x33, 0xe8, 0x48, 0x79, 0xb9, 0x70, 0x91,
    0x43, 0xe1, 0xf5, 0x93, 0xf0, 0x00, 0x00, 0x01,
];

/// Base field size (q) for BN254
/// q = 21888242871839275222246405745257275088696311157297823662689037894645226208583
const BASE_FIELD: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29,
    0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x97, 0x81, 0x6a, 0x91, 0x68, 0x71, 0xca, 0x8d,
    0x3c, 0x20, 0x8c, 0x16, 0xd8, 0x7c, 0xfd, 0x47,
];

fn is_valid_scalar(val: &[u8; 32]) -> bool {
    for i in 0..32 {
        if val[i] < SCALAR_FIELD[i] {
            return true;
        }
        if val[i] > SCALAR_FIELD[i] {
            return false;
        }
    }
    false // equal to r is not valid
}

fn field_negate(a: &[u8]) -> [u8; 32] {
    let mut result = [0u8; 32];
    let mut borrow = false;
    for i in (0..32).rev() {
        let (diff, b1) = BASE_FIELD[i].overflowing_sub(a[i]);
        let (diff2, b2) = diff.overflowing_sub(if borrow { 1 } else { 0 });
        result[i] = diff2;
        borrow = b1 || b2;
    }
    // If a == 0, return 0 (not q)
    let all_zero = a.iter().all(|&b| b == 0);
    if all_zero {
        return [0u8; 32];
    }
    result
}

fn alt_bn128_g1_add(a: &[u8; 64], b: &[u8; 64]) -> Result<[u8; 64]> {
    let mut input = [0u8; 128];
    input[..64].copy_from_slice(a);
    input[64..128].copy_from_slice(b);

    let result = alt_bn128_addition(&input)
        .map_err(|_| error!(VerifierError::Bn128AdditionFailed))?;

    let mut out = [0u8; 64];
    out.copy_from_slice(&result[..64]);
    Ok(out)
}

fn alt_bn128_g1_mul(point: &[u8; 64], scalar: &[u8; 32]) -> Result<[u8; 64]> {
    let mut input = [0u8; 96];
    input[..64].copy_from_slice(point);
    input[64..96].copy_from_slice(scalar);

    let result = alt_bn128_multiplication(&input)
        .map_err(|_| error!(VerifierError::Bn128MultiplicationFailed))?;

    let mut out = [0u8; 64];
    out.copy_from_slice(&result[..64]);
    Ok(out)
}

fn alt_bn128_pairing(input: &[u8]) -> Result<bool> {
    let result = alt_bn128_pairing_syscall(input)
        .map_err(|_| error!(VerifierError::Bn128PairingFailed))?;

    // Pairing returns 1 (true) if the check passes
    Ok(result[31] == 1 && result[..31].iter().all(|&b| b == 0))
}

