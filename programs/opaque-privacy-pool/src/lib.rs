// Privacy pool on Solana (spec/privacy-pool.md): amount privacy via the Privacy Pools
// (Buterin/Soleimani association-set) construction. Deposits insert
// commitment = Poseidon(value, label, Poseidon(nullifier, secret)) into an append-only
// Poseidon Merkle tree (label = Poseidon(scope, leafIndex), contract-assigned); a
// withdrawal verifies the Groth16 `withdrawal` circuit proof (state membership +
// association-set membership + nullifier + value accounting + context binding) via the
// alt_bn254 syscalls, consumes the nullifier, inserts the remainder, and pays out.
//
// Standalone per-chain pool (NOT cross-chain mirrored): deposits/withdrawals are
// chain-local. Testnet only; mainnet gated on audits + a production trusted setup.

pub const LEVELS: usize = 20;
/// Recent-root ring buffer size. Kept modest so the `Pool` account (held inline in the
/// instruction `Context`) stays within the BPF 4 KB stack frame; 16 roots is ample for
/// proofs made against a slightly stale root.
pub const ROOT_HISTORY: usize = 16;
/// BN254 scalar field r, big-endian.
const SCALAR_FIELD: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29, 0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x28, 0x33, 0xe8, 0x48, 0x79, 0xb9, 0x70, 0x91, 0x43, 0xe1, 0xf5, 0x93, 0xf0, 0x00, 0x00, 0x01,
];
const BASE_FIELD: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29, 0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x97, 0x81, 0x6a, 0x91, 0x68, 0x71, 0xca, 0x8d, 0x3c, 0x20, 0x8c, 0x16, 0xd8, 0x7c, 0xfd, 0x47,
];
const MAX_VALUE: u128 = 1u128 << 127; // value < 2^127 (well within the circuit's 2^128 bound)

#[program]
pub mod opaque_privacy_pool {
    use super::*;

    /// Initialize the pool: derive `scope`, cache the zero-subtree roots, set the empty
    /// root and the ASP authority. One-time.
    pub fn initialize(ctx: Context<Initialize>, asp_authority: Pubkey) -> Result<()> {
        // Only the program's upgrade authority (the deployer) may initialize (OPQ-007). On an
        // ephemeral test validator the program is loaded with the default (all-zero) authority
        // that nobody controls, so also accept that — a real upgradeable deployment always has
        // a non-zero authority, so this never loosens a live deployment.
        let ua = ctx.accounts.program_data.upgrade_authority_address;
        require!(
            ua == Some(ctx.accounts.payer.key()) || ua == Some(Pubkey::default()),
            PoolError::Unauthorized
        );

        let pool = &mut ctx.accounts.pool;
        pool.bump = ctx.bumps.pool;
        pool.asp_authority = asp_authority;
        pool.asp_root = [0u8; 32];
        pool.next_index = 0;
        pool.current_root_index = 0;

        // scope = keccak256("opaque-pool-scope" || pool_pubkey) with the top 3 bits
        // cleared so it is a valid field element (< 2^253 < r). Deterministic; the
        // prover recomputes it the same way to build `label`.
        let mut scope = keccak::hashv(&[b"opaque-pool-scope", pool.key().as_ref()]).to_bytes();
        scope[0] &= 0x1f;
        pool.scope = scope;

        // ZEROS is the precomputed zero-subtree cache (Poseidon doublings of 0); the
        // empty tree's filled subtrees are the zero subtrees and the root is ZEROS[LEVELS].
        for (slot, zero) in pool.filled_subtrees.iter_mut().zip(ZEROS.iter()) {
            *slot = *zero;
        }
        pool.roots[0] = ZEROS[LEVELS];
        Ok(())
    }

    /// Update the ASP association-set root (authority only).
    pub fn set_asp_root(ctx: Context<SetAspRoot>, new_root: [u8; 32]) -> Result<()> {
        ctx.accounts.pool.asp_root = new_root;
        emit!(AspRootUpdated { new_root });
        Ok(())
    }

    /// Deposit `value` lamports against `precommitment = Poseidon(nullifier, secret)`.
    /// The program assigns `label = Poseidon(scope, leafIndex)` and inserts
    /// `commitment = Poseidon(value, label, precommitment)`.
    pub fn deposit(ctx: Context<Deposit>, precommitment: [u8; 32], value: u64) -> Result<()> {
        require!(value > 0, PoolError::ZeroValue);
        require!((value as u128) < MAX_VALUE, PoolError::ValueTooLarge);
        require!(is_valid_scalar(&precommitment), PoolError::InvalidField);

        let leaf_index = ctx.accounts.pool.next_index;
        let label = poseidon2(&ctx.accounts.pool.scope, &index_to_field(leaf_index))?;
        let commitment = poseidon3(&value_to_field(value), &label, &precommitment)?;

        // Move the deposit into the pool PDA (vault).
        invoke(
            &system_instruction::transfer(
                &ctx.accounts.depositor.key(),
                &ctx.accounts.pool.key(),
                value,
            ),
            &[
                ctx.accounts.depositor.to_account_info(),
                ctx.accounts.pool.to_account_info(),
                ctx.accounts.system_program.to_account_info(),
            ],
        )?;

        insert(&mut ctx.accounts.pool, commitment)?;
        emit!(DepositEvent {
            commitment,
            label,
            value,
            leaf_index,
        });
        Ok(())
    }

    /// Withdraw via a Groth16 `withdrawal` proof. Public signals (order matches the
    /// circuit): [withdrawn_value, state_root, asp_root, nullifier_hash, new_commitment,
    /// context]. Verifies the proof, checks the roots + nullifier, consumes the
    /// nullifier, inserts the remainder, and pays out (recipient gets withdrawn-fee).
    #[allow(clippy::too_many_arguments)]
    pub fn withdraw(
        ctx: Context<Withdraw>,
        proof_a: [u8; 64],
        proof_b: [u8; 128],
        proof_c: [u8; 64],
        withdrawn_value: u64,
        state_root: [u8; 32],
        nullifier_hash: [u8; 32],
        new_commitment: [u8; 32],
        fee: u64,
    ) -> Result<()> {
        require!(fee <= withdrawn_value, PoolError::FeeExceedsWithdrawn);
        require!(is_known_root(&ctx.accounts.pool, &state_root), PoolError::UnknownStateRoot);

        let recipient = ctx.accounts.recipient.key();
        let fee_recipient = ctx.accounts.fee_recipient.key();
        let context = compute_context(&recipient, &fee_recipient, fee, &ctx.accounts.pool.scope);

        // Public signals in circuit order.
        let pub_signals: [[u8; 32]; 6] = [
            value_to_field(withdrawn_value),
            state_root,
            ctx.accounts.pool.asp_root,
            nullifier_hash,
            new_commitment,
            context,
        ];
        require!(
            verify_withdrawal(proof_a, proof_b, proof_c, &pub_signals)?,
            PoolError::InvalidProof
        );

        // The nullifier PDA is init-once: a replayed nullifier fails at account creation.
        ctx.accounts.nullifier.bump = ctx.bumps.nullifier;

        insert(&mut ctx.accounts.pool, new_commitment)?;

        // Pay out from the pool PDA (vault). recipient gets withdrawn_value - fee.
        let to_recipient = withdrawn_value - fee;
        if to_recipient > 0 {
            move_lamports(
                &ctx.accounts.pool.to_account_info(),
                &ctx.accounts.recipient,
                to_recipient,
            )?;
        }
        if fee > 0 {
            move_lamports(
                &ctx.accounts.pool.to_account_info(),
                &ctx.accounts.fee_recipient,
                fee,
            )?;
        }

        emit!(WithdrawalEvent {
            nullifier_hash,
            new_commitment,
            withdrawn_value,
            recipient,
        });
        Ok(())
    }
}

// ---------------------------------------------------------------------------
// Merkle tree (append-only, Poseidon, cached zeros — O(levels) per insert)
// ---------------------------------------------------------------------------

fn insert(pool: &mut Account<Pool>, leaf: [u8; 32]) -> Result<()> {
    require!((pool.next_index as usize) < (1usize << LEVELS), PoolError::TreeFull);
    let mut current_index = pool.next_index;
    let mut current_hash = leaf;
    // Index-driven: each level mixes the running hash with its filled-subtree sibling
    // or the precomputed zero subtree, depending on the bit of current_index.
    #[allow(clippy::needless_range_loop)]
    for i in 0..LEVELS {
        let (left, right) = if current_index & 1 == 0 {
            pool.filled_subtrees[i] = current_hash;
            (current_hash, ZEROS[i])
        } else {
            (pool.filled_subtrees[i], current_hash)
        };
        current_hash = poseidon2(&left, &right)?;
        current_index /= 2;
    }
    let new_root_index = (pool.current_root_index + 1) % (ROOT_HISTORY as u32);
    pool.current_root_index = new_root_index;
    pool.roots[new_root_index as usize] = current_hash;
    pool.next_index += 1;
    Ok(())
}

fn is_known_root(pool: &Account<Pool>, root: &[u8; 32]) -> bool {
    if *root == [0u8; 32] {
        return false;
    }
    let mut i = pool.current_root_index as usize;
    loop {
        if pool.roots[i] == *root {
            return true;
        }
        if i == 0 {
            i = ROOT_HISTORY;
        }
        i -= 1;
        if i == pool.current_root_index as usize {
            break;
        }
    }
    false
}

// ---------------------------------------------------------------------------
// Poseidon (circomlib-compatible via light-poseidon) + field helpers
// ---------------------------------------------------------------------------

/// circomlib-compatible Poseidon over BN254 (Bn254X5) via the native Solana syscall.
fn poseidon2(a: &[u8; 32], b: &[u8; 32]) -> Result<[u8; 32]> {
    let h = poseidon_hashv(Parameters::Bn254X5, Endianness::BigEndian, &[a, b])
        .map_err(|_| error!(PoolError::PoseidonError))?;
    Ok(h.to_bytes())
}

fn poseidon3(a: &[u8; 32], b: &[u8; 32], c: &[u8; 32]) -> Result<[u8; 32]> {
    let h = poseidon_hashv(Parameters::Bn254X5, Endianness::BigEndian, &[a, b, c])
        .map_err(|_| error!(PoolError::PoseidonError))?;
    Ok(h.to_bytes())
}

/// u64 → big-endian 32-byte field element.
fn value_to_field(v: u64) -> [u8; 32] {
    let mut out = [0u8; 32];
    out[24..32].copy_from_slice(&v.to_be_bytes());
    out
}

/// leaf index → field element.
fn index_to_field(i: u32) -> [u8; 32] {
    let mut out = [0u8; 32];
    out[28..32].copy_from_slice(&i.to_be_bytes());
    out
}

/// context = keccak256(recipient || fee_recipient || fee_be8 || scope), top 3 bits
/// cleared so it is a valid field element. The prover recomputes this identically.
fn compute_context(recipient: &Pubkey, fee_recipient: &Pubkey, fee: u64, scope: &[u8; 32]) -> [u8; 32] {
    let mut ctx = keccak::hashv(&[
        recipient.as_ref(),
        fee_recipient.as_ref(),
        &fee.to_be_bytes(),
        scope,
    ])
    .to_bytes();
    ctx[0] &= 0x1f;
    ctx
}

fn is_valid_scalar(v: &[u8; 32]) -> bool {
    for i in 0..32 {
        if v[i] < SCALAR_FIELD[i] {
            return true;
        }
        if v[i] > SCALAR_FIELD[i] {
            return false;
        }
    }
    false
}

/// Move lamports out of a program-owned account (deposits sit above rent).
fn move_lamports(from: &AccountInfo, to: &AccountInfo, amount: u64) -> Result<()> {
    **from.try_borrow_mut_lamports()? -= amount;
    **to.try_borrow_mut_lamports()? += amount;
    Ok(())
}

// ---------------------------------------------------------------------------
// Groth16 verification (alt_bn254 syscalls; pool withdrawal vkey)
// ---------------------------------------------------------------------------

fn verify_withdrawal(
    proof_a: [u8; 64],
    proof_b: [u8; 128],
    proof_c: [u8; 64],
    pub_signals: &[[u8; 32]; 6],
) -> Result<bool> {
    for s in pub_signals {
        require!(is_valid_scalar(s), PoolError::InvalidPublicSignal);
    }
    let mut vk_x = VK_IC_POOL[0];
    for i in 0..6 {
        let m = alt_bn128_g1_mul(&VK_IC_POOL[i + 1], &pub_signals[i])?;
        vk_x = alt_bn128_g1_add(&vk_x, &m)?;
    }
    run_pairing_check(proof_a, proof_b, proof_c, vk_x)
}

fn run_pairing_check(
    proof_a: [u8; 64],
    proof_b: [u8; 128],
    proof_c: [u8; 64],
    vk_x: [u8; 64],
) -> Result<bool> {
    let mut neg_a = proof_a;
    let neg_a_y = field_negate(&proof_a[32..64]);
    neg_a[32..64].copy_from_slice(&neg_a_y);

    let mut input = Vec::with_capacity(768);
    input.extend_from_slice(&neg_a);
    input.extend_from_slice(&proof_b);
    input.extend_from_slice(&VK_ALPHA_POOL);
    input.extend_from_slice(&VK_BETA_POOL);
    input.extend_from_slice(&vk_x);
    input.extend_from_slice(&VK_GAMMA_POOL);
    input.extend_from_slice(&proof_c);
    input.extend_from_slice(&VK_DELTA_POOL);

    let result =
        alt_bn128_pairing_syscall(&input).map_err(|_| error!(PoolError::PairingFailed))?;
    Ok(result[31] == 1 && result[..31].iter().all(|&b| b == 0))
}

fn field_negate(a: &[u8]) -> [u8; 32] {
    if a.iter().all(|&b| b == 0) {
        return [0u8; 32];
    }
    let mut result = [0u8; 32];
    let mut borrow = false;
    for i in (0..32).rev() {
        let (d1, b1) = BASE_FIELD[i].overflowing_sub(a[i]);
        let (d2, b2) = d1.overflowing_sub(if borrow { 1 } else { 0 });
        result[i] = d2;
        borrow = b1 || b2;
    }
    result
}

fn alt_bn128_g1_add(a: &[u8; 64], b: &[u8; 64]) -> Result<[u8; 64]> {
    let mut input = [0u8; 128];
    input[..64].copy_from_slice(a);
    input[64..].copy_from_slice(b);
    let r = alt_bn128_addition(&input).map_err(|_| error!(PoolError::Bn128Failed))?;
    let mut out = [0u8; 64];
    out.copy_from_slice(&r[..64]);
    Ok(out)
}

fn alt_bn128_g1_mul(point: &[u8; 64], scalar: &[u8; 32]) -> Result<[u8; 64]> {
    let mut input = [0u8; 96];
    input[..64].copy_from_slice(point);
    input[64..].copy_from_slice(scalar);
    let r = alt_bn128_multiplication(&input).map_err(|_| error!(PoolError::Bn128Failed))?;
    let mut out = [0u8; 64];
    out.copy_from_slice(&r[..64]);
    Ok(out)
}

