/**
 * Generate the Solana half of `@opaquecash/deployments` from `Anchor.toml`.
 *
 *   npm run generate            # writes into ../sdk/packages/deployments
 *   OPAQUE_SDK_DIR=/path/sdk npm run generate
 *
 * Program ids are parsed from the `[programs.<cluster>]` tables (the same ids as the
 * `declare_id!` macros). The Wormhole Core Bridge is an external well-known program,
 * recorded per cluster below (re-verify against the Wormhole docs at deploy time).
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SDK_DIR = resolve(process.env.OPAQUE_SDK_DIR ?? join(ROOT, "..", "sdk"));
const OUT_DIR = join(SDK_DIR, "packages", "deployments", "src", "generated");

/** Clusters exported to the SDK (localnet is test-only). */
const CLUSTERS = ["devnet"];

/** Wormhole Core Bridge program per cluster (docs.wormhole.com/reference/contract-addresses). */
const WORMHOLE_CORE = {
  devnet: "3u8hJUVTA4jH1wYAyUur7FFZVQ8H635K3tSHHF4ssjQ5",
  "mainnet-beta": "worm2ZoG2kUd4vFXhvjh93UUH596ayRfgQ2MgjNMTth",
};

function parseAnchorPrograms(toml, cluster) {
  const section = toml.match(
    new RegExp(`\\[programs\\.${cluster}\\]([\\s\\S]*?)(?:\\n\\[|$)`),
  );
  if (!section) throw new Error(`No [programs.${cluster}] table in Anchor.toml`);
  const programs = {};
  for (const line of section[1].split("\n")) {
    const m = line.match(/^\s*([a-z0-9_]+)\s*=\s*"([1-9A-HJ-NP-Za-km-z]{32,44})"/);
    if (m) programs[m[1]] = m[2];
  }
  return programs;
}

/** snake_case (Anchor.toml) → camelCase (SDK field names). */
function camel(s) {
  return s.replace(/_([a-z0-9])/g, (_, c) => c.toUpperCase());
}

const toml = readFileSync(join(ROOT, "Anchor.toml"), "utf8");
const entries = CLUSTERS.map((cluster) => {
  const programs = parseAnchorPrograms(toml, cluster);
  const required = [
    "stealth_registry",
    "stealth_announcer",
    "schema_registry",
    "attestation_engine_v2",
    "groth16_verifier",
    "reputation_verifier",
    "uab_receiver",
  ];
  for (const key of required) {
    if (!programs[key]) throw new Error(`Missing ${key} in [programs.${cluster}]`);
  }
  const fields = required
    .map((key) => `    ${camel(key)}: "${programs[key]}",`)
    .join("\n");
  return `  "${cluster}": {
    cluster: "${cluster}",
${fields}
    wormholeCore: "${WORMHOLE_CORE[cluster]}",
  },`;
}).join("\n");

const out = `// AUTO-GENERATED by solana/scripts/generate-deployments.mjs — do not edit.
// Source of truth: solana/Anchor.toml [programs.<cluster>] tables.
import type { SolanaProgramIds } from "../types.js";

/** Opaque Solana program ids keyed by cluster. */
export const SOLANA_PROGRAM_IDS: Record<string, SolanaProgramIds> = {
${entries}
};
`;

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(join(OUT_DIR, "solana.ts"), out);
console.log(`Wrote ${CLUSTERS.length} Solana cluster(s) to ${OUT_DIR}/solana.ts`);
