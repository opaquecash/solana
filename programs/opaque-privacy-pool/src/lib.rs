#[account]
pub struct Pool {
    pub asp_authority: Pubkey,
    pub scope: [u8; 32],
    pub asp_root: [u8; 32],
    pub next_index: u32,
    pub current_root_index: u32,
    pub filled_subtrees: [[u8; 32]; LEVELS],
    pub roots: [[u8; 32]; ROOT_HISTORY],
    pub bump: u8,
}

impl Pool {
    pub const LEN: usize = 32 + 32 + 32 + 4 + 4
        + 32 * LEVELS
        + 32 * ROOT_HISTORY
        + 1;
}

#[account]
pub struct Nullifier {
    pub bump: u8,
}

impl Nullifier {
    pub const LEN: usize = 1;
}

#[event]
pub struct DepositEvent {
    pub commitment: [u8; 32],
    pub label: [u8; 32],
    pub value: u64,
    pub leaf_index: u32,
}

#[event]
pub struct WithdrawalEvent {
    pub nullifier_hash: [u8; 32],
    pub new_commitment: [u8; 32],
    pub withdrawn_value: u64,
    pub recipient: Pubkey,
}

#[event]
pub struct AspRootUpdated {
    pub new_root: [u8; 32],
}

#[error_code]
pub enum PoolError {
    #[msg("Deposit value must be non-zero")]
    ZeroValue,
    #[msg("Deposit value is too large")]
    ValueTooLarge,
    #[msg("Value is not a valid field element")]
    InvalidField,
    #[msg("Merkle tree is full")]
    TreeFull,
    #[msg("Unknown state root")]
    UnknownStateRoot,
    #[msg("Fee exceeds the withdrawn value")]
    FeeExceedsWithdrawn,
    #[msg("Public signal is not in the scalar field")]
    InvalidPublicSignal,
    #[msg("Invalid withdrawal proof")]
    InvalidProof,
    #[msg("Poseidon hashing failed")]
    PoseidonError,
    #[msg("BN254 group operation failed")]
    Bn128Failed,
    #[msg("BN254 pairing failed")]
    PairingFailed,
    #[msg("Unauthorized: caller is not the program upgrade authority")]
    Unauthorized,
}

